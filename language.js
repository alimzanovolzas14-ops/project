function setLang(l) {
  lang = l;
  // Update all language toggle buttons
  document.querySelectorAll('.lang-btn, .sidebar-lang button').forEach(function(b) {
    b.classList.toggle('active', b.textContent.trim().toLowerCase() === l);
  });
  applyLang();
}

function applyLang() {
  var t = T[lang] || T['ru'];

  // 1. Translate [data-i18n] keys from T object — everywhere on the page
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var k = el.getAttribute('data-i18n');
    if (t[k] === undefined) return;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = t[k];
    } else {
      el.textContent = t[k];
    }
  });

  // 2. Translate [data-ph] placeholder attributes
  document.querySelectorAll('[data-ph]').forEach(function(el) {
    var k = el.getAttribute('data-ph');
    if (t[k] !== undefined) el.placeholder = t[k];
  });

  // 3. Translate data-ru / data-kz / data-en attributes on ALL elements ALL pages
  //    Only leaf nodes (no children) to avoid breaking layouts
  document.querySelectorAll('[data-' + lang + ']').forEach(function(el) {
    var val = el.getAttribute('data-' + lang);
    if (!val || !val.trim()) return;
    if (el.children.length > 0) return;   // skip containers
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });

  // 4. Re-render dynamic JS-generated sections
  var fns = ['renderProjects','renderNews','renderJobs','renderStack',
             'renderEmployees','renderPartners','renderFAQ','renderCases',
             'renderQuiz','renderIncident','renderUptime','drawCityMap',
             'renderEduResources','renderWeather'];
  fns.forEach(function(fn) {
    if (typeof window[fn] === 'function') window[fn]();
  });

  // 5. Sidebar group labels
  var gmap = {
    'Компания':     { kz: 'Компания',      en: 'Company'    },
    'Деятельность': { kz: 'Қызмет',        en: 'Activity'   },
    'Технологии':   { kz: 'Технологиялар', en: 'Technology' },
    'Команда':      { kz: 'Команда',        en: 'Team'       },
    'Медиа':        { kz: 'Медиа',          en: 'Media'      },
  };
  document.querySelectorAll('[data-sbg]').forEach(function(el) {
    var g = el.getAttribute('data-sbg');
    el.textContent = (gmap[g] && gmap[g][lang]) ? gmap[g][lang] : g;
  });

  // 6. Breadcrumb
  if (typeof MENU_DATA !== 'undefined' && typeof currentPage !== 'undefined') {
    var info = MENU_DATA[currentPage];
    var name = info ? (info[lang] || info.ru || currentPage) : currentPage;
    var bc = document.getElementById('breadcrumb');
    if (bc) bc.innerHTML =
      '<a onclick="navigate(&quot;home&quot;)">DDC</a>' +
      '<span class="breadcrumb-sep">›</span>' +
      '<span>' + name + '</span>';
  }

  // 7. AI assistant greeting
  var greet = document.querySelector('#aiMsgs .ai-msg.bot');
  if (greet && t.aiGreet) greet.textContent = t.aiGreet;
}

// Called each time we navigate to a page — ensures freshly shown page is translated
function applyLangToPage(pid) {
  var pg = document.getElementById('page-' + pid);
  if (!pg) return;
  var t = T[lang] || T['ru'];

  // data-i18n
  pg.querySelectorAll('[data-i18n]').forEach(function(el) {
    var k = el.getAttribute('data-i18n');
    if (t[k] === undefined) return;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') el.placeholder = t[k];
    else el.textContent = t[k];
  });

  // data-ru/kz/en leaf nodes
  pg.querySelectorAll('[data-' + lang + ']').forEach(function(el) {
    var val = el.getAttribute('data-' + lang);
    if (!val || !val.trim()) return;
    if (el.children.length > 0) return;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') el.placeholder = val;
    else el.textContent = val;
  });

  // Filter buttons (.proj-tab, .page-tab-btn)
  pg.querySelectorAll('.proj-tab, .page-tab-btn').forEach(function(btn) {
    var v = btn.getAttribute('data-' + lang);
    if (v && btn.children.length === 0) btn.textContent = v;
  });
}
